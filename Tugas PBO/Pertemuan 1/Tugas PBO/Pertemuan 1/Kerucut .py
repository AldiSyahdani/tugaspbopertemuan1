print ("Aplikasi menghitung luas selimut, luas permukaan dan volume Kerucut")

# Atur nilai variabel
Jarijari = 12
Tinggi = 9
Sisi = 4

# Rumus
LuasSelimut = 3.14 * Jarijari * Sisi
LuasPermukaan = (3.14 * Jarijari * Sisi) + (3.14 * Jarijari**2)
volume = 1/3 * 3.14 * Jarijari**2 * Tinggi

# Output
print("Jari Jari= ",Jarijari)
print("Tinggi= ",Tinggi)
print("Sisi= ",Sisi)
print("Hasilnya Adalah")
print("Luas Selimut =",LuasSelimut)
print("Luas Permukaan =",LuasPermukaan)
print("Volume =",volume)
