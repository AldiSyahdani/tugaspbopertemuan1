print ("Aplikasi menghitung luas selimut, luas permukaan dan volume Tabung")

# Atur nilai variabel
Jarijari = 12
Tinggi = 9

# Rumus
LuasSelimut = 2 * 3.14 * Jarijari * Tinggi
LuasPermukaan = (2 * 3.14 * Jarijari * Tinggi) + (2 * 3.14 * Jarijari**2)
volume = 3.14 * (Jarijari**2) * Tinggi

# Output
print("Jari Jari= ",Jarijari)
print("Tinggi= ",Tinggi)
print("Hasilnya Adalah")
print("Luas Selimut =",LuasSelimut)
print("Luas Permukaan =",LuasPermukaan)
print("Volume =",volume)
